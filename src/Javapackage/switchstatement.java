package Javapackage;

public class switchstatement {

	
	
	public static void main(String[] args)
	{
		int i=4;
		
		switch (i) {
		case 4: {
			
			System.out.println("the value is 4 " );
		}
		case 'c':
		{
			System.out.println("the vlaue is d");
		}
		default:
			System.out.println("the value is default");
		}
		
		
	}
}
